// push(): Adds one or more elements to the end of an array and returns the new length of the array.

let myArray = ['apple', 'banana', 'orange'];

// Add an element to the end of the array using push()
myArray.push('grape');

console.log(myArray); // Output: ["apple", "banana", "orange", "grape"]

// Add multiple elements to the end of the array using push()
myArray.push('pear', 'kiwi');

console.log(myArray); // Output: ["apple", "banana", "orange", "grape", "pear", "kiwi"]
